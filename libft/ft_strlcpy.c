#include "libft.h"

size_t	ft_strlcpy(char *cpy, char *src, size_t l)
{
	size_t	i;

	i = 0;
	if (l > 0)
	{
		while (*(src + i) && i < (l - 1))
		{
			*(cpy + i) = *(src + i);
			i++;
		}
		*(cpy + i) = '\0';
	}
	while (*(src + i))
		i++;
	return (i);
}
